    private void DBasisMTItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_DBasisMTItemStateChanged
        int size = DBasisMT.getItemCount();
        if(size > 0){
            String tipeTower = DTT.getSelectedItem().toString();
            String basisTower = DBasisMT.getSelectedItem().toString();
            if(tipeTower != "- pilih -" && basisTower != "- pilih -"){
                System.out.println(basisTower);
                MTModel material = hsp.getMaterialTower(tipeTower, basisTower);
                TBeratErect.setText(material.getBerat()+" KG");
                double hargaPermenperin = Double.parseDouble(material.getHarga_permenperin());
                THargaMT.setText(utils.getCurrencyFormat(String.valueOf(hargaPermenperin)));
                THargaErect.setText(utils.getCurrencyFormat(material.getHarga_erection()));
            }
        }
    }//GEN-LAST:event_DBasisMTItemStateChanged

