    private void DTTItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_DTTItemStateChanged
        String tipeTower = DTT.getSelectedItem().toString();
        if(tipeTower != "- pilih -"){
            //load data basis berdasarkan tipe
            hsp.loadBasisMaterial(tipeTower);
            DBasisMT.removeAllItems();
            DBasisMT.addItem("- pilih -");
            List<String> basisMaterial = hsp.getNamaMT();
            for(String basis : basisMaterial){
                DBasisMT.addItem(basis);
            }
            dropQuan.setSelectedIndex(1);
            dropSat.setSelectedIndex(1);
            Utils ut = new Utils();
            String currentcy = String.valueOf("461689150.909928");
            THargaKMR.setText(ut.getCurrencyFormat(currentcy));
            DBasisMT.setSelectedIndex(0);
        }
        
    }//GEN-LAST:event_DTTItemStateChanged

